/**
  * @brief This function handles TIM5 global interrupt.
  */
void TIM5_IRQHandler(void)
{
  /* USER CODE BEGIN TIM5_IRQn 0 */
  
  static uint16_t Pulse_Count = 1U;
  Pulse_Count ++;
  htim5.Instance->CR1 = htim5.Instance->CR1 | 0x1;
  if(Pulse_Number < Pulse_Count)
  {
    // htim5.Instance->CR1 = htim5.Instance->CR1 ^ 0x8;
    Pulse_Count = 1U;
    OK = true;
    htim5.Instance->CR1 = htim5.Instance->CR1 & 0xFE;
    htim5.Instance->CNT = 0x00;
  }

  /* USER CODE END TIM5_IRQn 0 */
  HAL_TIM_IRQHandler(&htim5);
  /* USER CODE BEGIN TIM5_IRQn 1 */
  __HAL_TIM_CLEAR_IT(&htim5, TIM_IT_UPDATE);
  while(__HAL_TIM_GET_FLAG(&htim5, TIM_IT_UPDATE) != 0);
  /* USER CODE END TIM5_IRQn 1 */
}

